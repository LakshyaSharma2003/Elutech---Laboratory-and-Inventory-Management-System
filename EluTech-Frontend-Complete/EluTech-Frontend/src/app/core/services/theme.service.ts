@Injectable({ providedIn: 'root' })
export class ThemeService {

  currentTheme = signal<string>('default');
  private readonly LS_KEY = 'elutech_theme';

  constructor() {
    const saved = localStorage.getItem(this.LS_KEY) || 'default';
    this.applyTheme(saved);
  }

  applyTheme(themeId: string) {
    const vars = THEME_VARS[themeId];
    if (!vars) return;
    const root = document.documentElement;
    Object.entries(vars).forEach(([key, value]) => root.style.setProperty(key, value));
    // Set data-theme attribute for additional CSS hooks
    document.body.setAttribute('data-theme', themeId);
    const isDark = THEMES.find(t => t.id === themeId)?.dark || false;
    document.body.setAttribute('data-dark', isDark ? 'true' : 'false');
    this.currentTheme.set(themeId);
    localStorage.setItem(this.LS_KEY, themeId);
  }

  getThemes(): Theme[] { return THEMES; }
  getCurrentThemeObj(): Theme { return THEMES.find(t => t.id === this.currentTheme()) || THEMES[0]; }
}
